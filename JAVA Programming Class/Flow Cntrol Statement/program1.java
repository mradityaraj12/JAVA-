/// WAJP to print big of two numbers using if else Statement?
public class program1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a > b) {
            System.out.println(a + "is big");
        } else {
            System.out.println(b + "is big");
        }

    }

}
